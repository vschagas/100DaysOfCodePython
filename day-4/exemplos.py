''' Esse é um módulo de exemplos'''
import random
# Número inteiro aleaatório
radom_number = random.randint(1, 10)
print(radom_number)

# Número float aleatório
float_random = random.random()
print(float_random)

# Número float até valor determinado
float_random = random.random() * 5
print(float_random)

# Número aleatório inteiro dentro de um range

love_score = random.randint(1, 100)
print(f"Your love score is {love_score}")
